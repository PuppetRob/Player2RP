	`id` int(11) NOT NULL AUTO_INCREMENT,
	`citizenid` varchar(255) CHARACTER SET utf8mb4 NOT NULL,
	`plate` varchar(50) CHARACTER SET utf8mb4 NOT NULL,
	`shop` varchar(50) CHARACTER SET utf8mb4 NOT NULL,
	`model` varchar(50) CHARACTER SET utf8mb4 NOT NULL DEFAULT '0',
	`installments` int(11) NOT NULL DEFAULT 0,
	`price` int(11) NOT NULL DEFAULT 0,
	`status` int(11) NOT NULL DEFAULT 0,
	`date` timestamp NOT NULL DEFAULT current_timestamp(),
	PRIMARY KEY (`id`) USING BTREE,
	KEY `plate` (`plate`) USING BTREE,
	KEY `identifier` (`citizenid`) USING BTREE,
	KEY `store` (`shop`) USING BTREE,
	CONSTRAINT `FK_carshop_billing_carshop_stores` FOREIGN KEY (`shop`) REFERENCES `carshop_stores` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=42 DEFAULT CHARSET=latin1;

CREATE TABLE IF NOT EXISTS `carshop_stock` (
	`store` varchar(50) CHARACTER SET utf8mb4 NOT NULL DEFAULT '',
	`vehicle` varchar(50) CHARACTER SET utf8mb4 NOT NULL DEFAULT '',
	`price` int(11) NOT NULL,
