	KEY `FK_carshop_stock_carshop_stores` (`store`),
	CONSTRAINT `FK_carshop_stock_carshop_stores` FOREIGN KEY (`store`) REFERENCES `carshop_stores` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT `FK_carshop_stock_carshop_vehicles` FOREIGN KEY (`vehicle`) REFERENCES `carshop_vehicles` (`model`) ON DELETE CASCADE ON UPDATE CASCADE
